

SELECT 
SD2.D2_FILIAL,
SD2.D2_SERIE,
SD2.D2_DOC,
CONVERT(VARCHAR,CAST (SD2.D2_EMISSAO AS DATETIME),103) AS EMISSAO,
SD2.D2_COD,
SB1.B1_ZDESRDZ,
SD2.D2_ITEM,
SD2.D2_UM,
SD2.D2_QUANT,
(SB5.B5_LARG * SB5.B5_COMPR) AS M2,
(SB5.B5_LARG * SB5.B5_COMPR * SB5.B5_ALTURA) AS M3,
(SB5.B5_LARG * SB5.B5_COMPR) * SD2.D2_QUANT AS M2_TOTAL,
(SB5.B5_LARG * SB5.B5_COMPR * SB5.B5_ALTURA) * SD2.D2_QUANT AS M3_TOTAL,
SB1.B1_GRUPO,
SD2.D2_CLIENTE,
SA1.A1_NOME
FROM SD2010 SD2
LEFT JOIN SB1010 SB1 ON SB1.D_E_L_E_T_ = '' AND SB1.B1_COD = SD2.D2_COD
LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SA1.A1_COD = SD2.D2_CLIENTE AND SA1.A1_LOJA = SD2.D2_LOJA
LEFT JOIN SB5010 SB5 ON SB5.D_E_L_E_T_ = '' AND SB5.B5_COD = SD2.D2_COD
WHERE 1=1
AND SD2.D_E_L_E_T_ = ''
AND SD2.D2_TIPO <> 'D'
AND SD2.D2_ESTOQUE = 'S'
--AND SD2.D2_EMISSAO  BETWEEN '20210301' AND '20210324'
AND SB1.B1_ZDESRDZ  LIKE 'ISOPAINEL%' 
OR SB1.B1_ZDESRDZ  LIKE 'FRIGOPAINEL%'