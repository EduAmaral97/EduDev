

SELECT 
SE3.E3_SERIE	AS PREFIXO,
SE3.E3_NUM		AS TITULO,
SE3.E3_PARCELA	AS PARCELA,
SA3.A3_COD		AS COD_VEND,
SA3.A3_NOME		AS VENDEDOR,
SE3.E3_CODCLI	AS COD_CLI,
SA1.A1_NOME		AS CLIENTE,
SE3.E3_EMISSAO	AS EMISSAO,
SE1.E1_VENCORI	AS VENC_ORI,
SE1.E1_BAIXA	AS DT_BAIXA,
'  /  /  '		AS DT_PGMTO,
SE3.E3_PEDIDO	AS PEDIDO,
SE1.E1_VALOR	AS VLR_TITULO,
SE3.E3_BASE		AS VLR_BASE,
SE3.E3_PORC		AS PORC,
SE3.E3_COMIS	AS COMISSAO,
SE3.E3_BAIEMI	AS TIPO
FROM SE3010 SE3
LEFT JOIN SE1010 SE1 ON SE1.D_E_L_E_T_ = '' AND SE1.E1_FILIAL = SE3.E3_FILIAL AND SE1.E1_PREFIXO = SE3.E3_SERIE AND SE1.E1_NUM = SE3.E3_NUM AND SE1.E1_PARCELA = SE3.E3_PARCELA AND SE1.E1_TIPO = SE3.E3_TIPO AND SE3.E3_CODCLI = SE1.E1_CLIENTE AND SE3.E3_LOJA = SE1.E1_LOJA
LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SA1.A1_COD = SE3.E3_CODCLI AND SA1.A1_LOJA = SE3.E3_LOJA
LEFT JOIN SA3010 SA3 ON SA3.D_E_L_E_T_ = '' AND SA3.A3_COD = SE3.E3_VEND
WHERE 1=1
AND SE3.D_E_L_E_T_ = '' 
AND SE3.E3_FILIAL = '0103'
AND SE3.E3_EMISSAO BETWEEN '20210401' AND '20210430' 
AND SA3.A3_COD = '000030'