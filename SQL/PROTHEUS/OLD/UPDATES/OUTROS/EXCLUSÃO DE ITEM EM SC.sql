
DEFINE @V1 VARCHAR(4)
DEFINE @NUM VARCHAR(6)
DEFINE @PROD VARCHAR(15)

DECLARE @V1 VARCHAR(4)
DECLARE @NUM VARCHAR(6)
DECLARE @PROD VARCHAR(15)

SET @V1 = $(@V1)
SET @NUM = $(@NUM)
SET @PROD = $(@PROD)


UPDATE SC1010 SET D_E_L_E_T_ = '*'
FROM SC1010  
WHERE 1=1
AND D_E_L_E_T_ = ''
AND C1_FILIAL = @V1
AND C1_NUM = @NUM
AND C1_PRODUTO = @PROD


