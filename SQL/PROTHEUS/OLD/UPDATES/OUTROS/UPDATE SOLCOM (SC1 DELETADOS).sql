
SELECT
A.L2_EMISSAO,
CONVERT(VARCHAR,CAST(A.L2_EMISSAO AS DATETIME),103) AS EMISSAO,
A.L2_FILIAL,
A.L2_NUM,
A.L2_PRODUTO,
A.L2_SOLCOM,
B.C1_NUM,
B.C1_PRODUTO,
B.D_E_L_E_T_
--UPDATE SL2010 SET L2_SOLCOM = ''
FROM SL2010 A, SC1010 B
WHERE 1=1
AND A.L2_EMISSAO BETWEEN '20200101' AND '20200708'
AND A.D_E_L_E_T_ = ''
AND B.D_E_L_E_T_ = ''
AND A.L2_FILIAL = B.C1_FILIAL
AND A.L2_SOLCOM = B.C1_NUM
AND A.L2_PRODUTO = B.C1_PRODUTO
ORDER BY 1