
CREATE PROCEDURE ATUALIZACOMISSAO AS


UPDATE SE3010 SET E3_BASE = SE1.E1_VALOR
FROM SE3010 SE3 
LEFT JOIN SE1010 SE1 ON SE1.D_E_L_E_T_ = '' AND SE1.E1_FILIAL = SE3.E3_FILIAL AND SE1.E1_PREFIXO = SE3.E3_SERIE AND SE1.E1_NUM = SE3.E3_NUM AND SE1.E1_CLIENTE = SE3.E3_CODCLI AND SE1.E1_LOJA = SE3.E3_LOJA AND SE1.E1_PARCELA = SE3.E3_PARCELA AND SE1.E1_TIPO = SE3.E3_TIPO
WHERE 1=1
AND SE3.D_E_L_E_T_ = '' 
AND SE3.E3_CODCLI IN ('051', '053', '055', '057', '058')
AND SE3.E3_PARCELA = '01'
AND SE3.E3_BASE <> SE1.E1_VALOR

GO


