
DEFINE @1_AVISO ESCOLHA UMA OPÇÃO DE PESQUISA VARCHAR(1)
DEFINE @2_OP 1 - FORNECEDOR | 2 - TITULO VARCHAR(1)
DEFINE @99_OPC VARCHAR(1)
DEFINE @FORNECEDOR VARCHAR(6)
DEFINE @TITULO VARCHAR(6)

DECLARE @1_AVISO VARCHAR(1)
DECLARE @2_OP  VARCHAR(1)
DECLARE @99_OPC  VARCHAR(1)
DECLARE @FORNECEDOR  VARCHAR(6)
DECLARE @TITULO  VARCHAR(6)

SET @1_AVISO = $(@1_AVISO)
SET @2_OP = $(@2_OP)
SET @99_OPC = $(@99_OPC)
SET @FORNECEDOR = $(@FORNECEDOR)
SET @TITULO = $(@TITULO)


IF @99_OPC = '1'
BEGIN


SELECT 
E2_FILIAL,
E2_PREFIXO,
E2_NUM,
E2_TIPO,
E2_PARCELA,
E2_FORNECE,
E2_VALOR,
E2_VALLIQ,
E2_VLCRUZ,
E2_SALDO
FROM 
SE2010 
WHERE 1=1
AND D_E_L_E_T_ = '' 
AND E2_FORNECE = @FORNECEDOR

END


IF @99_OPC = '2'
BEGIN


SELECT 
E2_FILIAL,
E2_PREFIXO,
E2_NUM,
E2_TIPO,
E2_PARCELA,
E2_FORNECE,
E2_VALOR,
E2_VALLIQ,
E2_VLCRUZ,
E2_SALDO
FROM 
SE2010 
WHERE 1=1
AND D_E_L_E_T_ = '' 
AND E2_NUM = @TITULO



END








