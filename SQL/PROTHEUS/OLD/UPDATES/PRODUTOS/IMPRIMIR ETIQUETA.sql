

DEFINE @PROD VARCHAR(15)

DECLARE @PROD VARCHAR(15)

SET @PROD = $(@PROD)


UPDATE SB1010 
SET B1_ZIMPETQ = 'S'
FROM SB1010 
WHERE 1=1
AND D_E_L_E_T_ = ''
AND B1_COD = @PROD



