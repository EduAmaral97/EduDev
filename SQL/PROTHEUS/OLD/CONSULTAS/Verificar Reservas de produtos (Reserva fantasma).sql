SELECT 
SC9.C9_FILIAL,
SC9.C9_PEDIDO,
SC9.C9_ITEM,
SC9.C9_PRODUTO,
SB1.B1_ZDESRDZ,
SC6.C6_QTDVEN,
SC9.C9_QTDLIB,
SC9.C9_BLEST,
SA1.A1_COD,
SA1.A1_NOME
FROM SC9010 SC9 
LEFT JOIN SC6010 SC6 ON SC6.D_E_L_E_T_ = '' AND SC6.C6_FILIAL = SC9.C9_FILIAL AND SC6.C6_NUM = SC9.C9_PEDIDO AND SC6.C6_PRODUTO = SC9.C9_PRODUTO AND SC6.C6_ITEM = SC9.C9_ITEM
LEFT JOIN SB1010 SB1 ON SB1.D_E_L_E_T_ = '' AND SB1.B1_COD = SC9.C9_PRODUTO
LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SA1.A1_COD = SC9.C9_CLIENTE AND SA1.A1_LOJA = SC9.C9_LOJA
WHERE 1=1
AND SC9.D_E_L_E_T_ = '' 
AND SC9.C9_NFISCAL = ''
AND SC9.C9_BLEST = ''
AND SC9.C9_PRODUTO = 'GD031A'


