SELECT 
SC0.C0_FILIAL AS FILIAL,
SC0.C0_NUM	  AS NUM,
SC0.C0_PRODUTO AS CODPROD,
SB1.B1_ZDESRDZ AS PRODUTO,
SC0.C0_QUANT AS QTD,
SUA.UA_NUM AS ATENDIMENTO,
SA1.A1_COD AS COD_CLI,
SA1.A1_NOME AS CLIENTE
FROM SC0010 SC0
LEFT JOIN SB1010 SB1 ON SB1.D_E_L_E_T_ = '' AND SB1.B1_COD = SC0.C0_PRODUTO
LEFT JOIN SL2010 SL2 ON SL2.D_E_L_E_T_ = '' AND SL2.L2_FILIAL = SUBSTRING(SC0.C0_OBS,22,4) AND SL2.L2_NUM = SUBSTRING(SC0.C0_OBS,8,6) AND SL2.L2_PRODUTO = SC0.C0_PRODUTO AND SL2.L2_ORCRES = ''
LEFT JOIN SL1010 SL1 ON SL1.D_E_L_E_T_ = '' AND SL1.L1_FILIAL = SL2.L2_FILIAL AND SL1.L1_NUM = SL2.L2_NUM
LEFT JOIN SUA010 SUA ON SUA.D_E_L_E_T_ = '' AND SUA.UA_FILIAL = SL1.L1_FILIAL AND SUA.UA_NUM = SL1.L1_NUMATEN
LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SA1.A1_COD = SUA.UA_CLIENTE AND SA1.A1_LOJA = SUA.UA_LOJA
WHERE 1=1
AND SC0.D_E_L_E_T_ = ''
AND SC0.C0_PRODUTO = 'GD031A'


