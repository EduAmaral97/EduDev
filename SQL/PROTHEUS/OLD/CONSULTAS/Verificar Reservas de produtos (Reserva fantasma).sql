SELECT 
SB2.B2_FILIAL,
SB2.B2_COD,
SB1.B1_ZDESRDZ,
SB2.B2_LOCAL,
SB2.B2_QATU,
SB2.B2_RESERVA ,
(SB2.B2_QATU - SB2.B2_RESERVA) AS SLD_DISP,
SB2.D_E_L_E_T_,
SB2.R_E_C_N_O_,
SB2.R_E_C_D_E_L_
FROM SB2010 SB2
LEFT JOIN SB1010 SB1 ON SB1.D_E_L_E_T_ = '' AND SB1.B1_COD = SB2.B2_COD 
WHERE 1=1
AND SB2.D_E_L_E_T_ = '' 
AND SB2.B2_LOCAL = '01'
AND SB2.B2_COD = 'GD031A'



