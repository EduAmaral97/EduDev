

SELECT 
SE1.E1_FILIAL,
--CONVERT(VARCHAR,CAST(SE1.E1_EMISSAO AS DATETIME),103) AS EMISSAO,
SE1.E1_EMISSAO,
SE1.E1_PREFIXO,
SE1.E1_NUM,
SE1.E1_PARCELA,
SE1.E1_TIPO,
SE1.E1_CLIENTE,
SE1.E1_NOMCLI,
SA1.A1_NOME,
SE1.E1_VALOR, 
--SE1.E1_VALOR - E1_VALOR * 2 AS VLR_NEGATIVO,
SE3.E3_BASE,
SE3.E3_ZOBS
FROM SE1010 SE1
LEFT JOIN SE3010 SE3 ON SE3.D_E_L_E_T_ = ''  AND SE3.E3_SERIE = SE1.E1_PREFIXO AND SE3.E3_NUM = SE1.E1_NUM AND SE3.E3_CODCLI = SE1.E1_CLIENTE
LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SA1.A1_LOJA = SE1.E1_LOJA AND SA1.A1_COD = SE1.E1_CLIENTE 
WHERE 1=1
AND SE1.D_E_L_E_T_ = '' 
AND SE3.E3_NUM IS NULL
AND SE1.E1_TIPO = 'NCC'
AND YEAR(SE1.E1_EMISSAO) = 2020
--AND MONTH(SE1.E1_EMISSAO) = 9


