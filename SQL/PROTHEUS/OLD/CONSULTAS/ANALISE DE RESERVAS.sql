
/*----------ANALISAR RESERVAS----------*/

SELECT
SB2.B2_FILIAL  AS FILIAL,
SB1.B1_COD     AS CODPROD,
SB1.B1_ZDESRDZ AS PRODUTO,
SB2.B2_FILIAL  AS FILIAL_SALDO,
SB2.B2_QATU    AS SLD_FIS,
SB2.B2_RESERVA AS SLD_RES,
SB2.B2_QATU - SB2.B2_RESERVA AS SLD_DISP ,
SC9.C9_FILIAL  AS FILIAL_C9,
SC9.C9_PEDIDO  AS NUM_PV,
SC9.C9_QTDLIB  AS QTD_LIB,
SA1.A1_COD     AS COD_CLI,
SA1.A1_NOME    AS CLIENTE,
SC0.C0_FILIAL  AS FILIAL_C0,
SC0.C0_NUM	   AS RES_MANU,
SC0.C0_QUANT   AS QTD,
SC0.C0_QTDORIG AS QTD_ORIG,
SC0.C0_QTDPED  AS QTD_PED,
SA1X.A1_COD	   AS COD_CLI,
SA1X.A1_NOME   AS CLIENTE,
SC0.C0_OBS
FROM SB1010 SB1
LEFT JOIN SB2010 SB2 ON SB2.D_E_L_E_T_ = '' AND SB2.B2_COD = SB1.B1_COD AND SB2.B2_LOCAL = '01'
LEFT JOIN SC9010 SC9 ON SC9.D_E_L_E_T_ = '' AND SC9.C9_PRODUTO = SB1.B1_COD AND SC9.C9_NFISCAL = '' AND SC9.C9_BLEST = ''
LEFT JOIN SC6010 SC6 ON SC6.D_E_L_E_T_ = '' AND SC6.C6_FILIAL = SC9.C9_FILIAL AND SC6.C6_NUM = SC9.C9_PEDIDO AND SC6.C6_PRODUTO = SC9.C9_PRODUTO AND SC6.C6_ITEM = SC9.C9_ITEM
LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SA1.A1_COD = SC6.C6_CLI AND SA1.A1_LOJA = SC6.C6_LOJA
LEFT JOIN SC0010 SC0 ON SC0.D_E_L_E_T_ = '' AND SC0.C0_PRODUTO = SB1.B1_COD
LEFT JOIN SL1010 SL1 ON SL1.D_E_L_E_T_ = '' AND SL1.L1_FILIAL = SC0.C0_FILIAL AND SL1.L1_NUM = SUBSTRING(SC0.C0_OBS,8,6)
LEFT JOIN SA1010 SA1X ON SA1X.D_E_L_E_T_ = '' AND SA1X.A1_COD = SL1.L1_CLIENTE AND SA1X.A1_LOJA = SL1.L1_LOJA  
WHERE 1=1
AND SB1.D_E_L_E_T_ = ''
AND SB1.B1_COD = 'LB028F'


