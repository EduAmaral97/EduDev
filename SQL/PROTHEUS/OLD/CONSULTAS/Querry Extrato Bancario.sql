
SELECT 
    E5_RECPAG, 
    E5_CONTA,
    E5_AGENCIA, 
    E5_BANCO,
    E5_FILIAL, 
    E5_DATA, 
    E5_HISTOR, 
    E5_DOCUMEN, 
    E5_CLIFOR,
    CASE  
        WHEN E5_CLIENTE <> '' THEN ISNULL((SELECT A1_NOME FROM SA1010 WHERE D_E_L_E_T_ = '' AND A1_COD = E5_CLIFOR AND A1_LOJA = E5_LOJA),'') 
        WHEN E5_FORNECE <> '' THEN ISNULL((SELECT A2_NOME FROM SA2010 WHERE D_E_L_E_T_ = '' AND A2_COD = E5_CLIFOR AND A2_LOJA = E5_LOJA),'') 
        ELSE '' 
    END AS CLIENTE_FORNECEDOR,
    CONCAT(E5_PREFIXO, '-', E5_NUMERO, '-',E5_PARCELA) AS PREFIXO_TITULO,
    CASE 
        WHEN E5_RECPAG = 'P' THEN E5_VALOR 
        ELSE 0 
    END AS SAIDAS,
    CASE 
        WHEN E5_RECPAG = 'R' THEN E5_VALOR 
        ELSE 0 
    END AS ENTRADAS, 
    E5_RECONC,
    SE5010.E5_DTDISPO
FROM SE5010 
WHERE 1=1
    AND D_E_L_E_T_ = ''
    AND E5_BANCO = '237'  
    AND E5_AGENCIA = '0444'
    AND E5_CONTA = '0075299-1'
    AND E5_DATA BETWEEN '20210701' AND '20210706'
    --AND E5_NUMERO IN ('000036652','000030533')
    --AND E5_NUMERO = '000030727'
    --AND E5_RECONC = ''
ORDER BY 
    6, 
    12, 
    10


