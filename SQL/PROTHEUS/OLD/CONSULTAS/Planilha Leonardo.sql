
SELECT 
DISTINCT
SL2.L2_PRODUTO	AS CODPROD,
SB1.B1_ZDESRDZ	AS DESC_REDU,
SB1.B1_ZAPRES1	AS COD_APRE,
CONVERT(VARCHAR(MAX), CONVERT(VARBINARY(MAX),SB1.B1_ZTITWEB)) AS TIT_WEB,
SB1.B1_ZPROJET 	AS DESC_PROJETO,
SA2.A2_NREDUZ 	AS FORNECEDOR
FROM SUA010 SUA
LEFT JOIN SL1010 SL1 ON SL1.D_E_L_E_T_ = '' AND SL1.L1_FILIAL = SUA.UA_FILIAL AND SL1.L1_NUMATEN = SUA.UA_NUM AND SL1.L1_ORCRES = ''
LEFT JOIN SL2010 SL2 ON SL2.D_E_L_E_T_ = '' AND SL2.L2_FILIAL = SL1.L1_FILIAL AND SL2.L2_NUM = SL1.L1_NUM
LEFT JOIN SB1010 SB1 ON SB1.D_E_L_E_T_ = '' AND SB1.B1_COD = SL2.L2_PRODUTO
LEFT JOIN SA2010 SA2 ON SA2.D_E_L_E_T_ = '' AND SA2.A2_COD = SB1.B1_PROC AND SA2.A2_LOJA = SB1.B1_LOJPROC
WHERE 1=1
AND SUA.D_E_L_E_T_ = '' 
AND YEAR(SL1.L1_EMISNF) = 2021
AND SL1.L1_EMISNF <> ''
AND SUA.UA_ZTIPVND = '2'
AND SB1.B1_COD NOT IN
(
'40156',
'40158',
'40400',
'40404',
'40412',
'40421A',
'40432',
'40464'
)
