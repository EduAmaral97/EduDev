SELECT 
SF2.F2_CLIENTE AS CLIENTE,
SF2.F2_LOJA AS LOJA,
SA1.A1_NOME AS NOME,
SA1.A1_PESSOA AS TIPO_PESSOA,
SA1.A1_END AS ENDERECO,
SA1.A1_MUN AS MUNICIPIO,
SA1.A1_EST AS ESTADO,
SF2.F2_FILIAL AS FILIAL ,
CONVERT(VARCHAR,CAST(SF2.F2_EMISSAO AS DATETIME),103) AS DATA_FAT,
SF2.F2_SERIE AS SERIE,
SF2.F2_DOC AS NUMERO_DOC,
SF2.F2_VALBRUT AS VALOR_BRUTO
	FROM SF2010 SF2
	LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SF2.F2_CLIENTE = SA1.A1_COD AND SF2.F2_LOJA = SA1.A1_LOJA
WHERE SF2.D_E_L_E_T_ = ''	
AND SF2.F2_CLIENTE <> '000001'
ORDER BY SF2.F2_CLIENTE, SF2.F2_LOJA, SF2.F2_FILIAL, SF2.F2_SERIE, SF2.F2_DOC
	
/*----------------------------------------SOMA VALOR BRUTO-------------------------------------------------------*/

SELECT 
SF2.F2_CLIENTE AS CLIENTE,
SF2.F2_LOJA AS LOJA,
SA1.A1_NOME AS NOME,
SA1.A1_PESSOA AS TIPO_PESSOA,
SA1.A1_END AS ENDERECO,
SA1.A1_MUN AS MUNICIPIO,
SA1.A1_EST AS ESTADO,
SUM(SF2.F2_VALBRUT) AS VALOR_BRUTO
	FROM SF2010 SF2
	LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SF2.F2_CLIENTE = SA1.A1_COD AND SF2.F2_LOJA = SA1.A1_LOJA
WHERE SF2.D_E_L_E_T_ = ''	
AND SF2.F2_CLIENTE <> '000001'
GROUP BY SF2.F2_CLIENTE,SF2.F2_LOJA,SA1.A1_NOME,SA1.A1_PESSOA,SA1.A1_END,SA1.A1_MUN,SA1.A1_EST
ORDER BY SF2.F2_CLIENTE, SF2.F2_LOJA
	

