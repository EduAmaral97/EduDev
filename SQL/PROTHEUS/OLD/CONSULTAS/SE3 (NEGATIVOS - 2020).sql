
SELECT 
SE3.E3_FILIAL	AS FILIAL,
SE3.E3_SERIE	AS SERIE,
SE3.E3_NUM		AS TITULO,
SE3.E3_EMISSAO	AS EMI_E3,
SE3.E3_VENCTO	AS VENC_E3,
SE3.E3_BASE		AS VALOR,
SL1.L1_NUMATEN  AS ULT_ATEND,
SUA.UA_EMISSAO	AS EMI_ATEND,
SL1.L1_SERPED	AS SER_FIN,
SL1.L1_DOCPED	AS DOC_FIN,
SL1.L1_EMISNF   AS DT_FIN,
SL1.L1_CLIENTE  AS COD_CLI,
SA1.A1_NOME		AS CLIENTE,
SL1.L1_VEND		AS COD_VEND,
SA3.A3_NOME		AS VENDEDOR,
SE3.E3_ZOBS		AS OBS 
FROM SE3010 SE3
LEFT JOIN SL1010 SL1 ON SL1.D_E_L_E_T_ = '' AND SL1.L1_NUMATEN = (SELECT MAX(L1_NUMATEN) FROM SL1010 WHERE D_E_L_E_T_ = '' AND L1_CLIENTE = SE3.E3_CODCLI AND L1_LOJA = SE3.E3_LOJA AND L1_VEND = SE3.E3_VEND AND L1_ORCRES = '' AND L1_DOCPED <> '')
LEFT JOIN SUA010 SUA ON SUA.D_E_L_E_T_ = '' AND SUA.UA_FILIAL = SL1.L1_FILIAL AND SUA.UA_NUM = SL1.L1_NUMATEN
LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SA1.A1_COD = SL1.L1_CLIENTE AND SA1.A1_LOJA = SL1.L1_LOJA
LEFT JOIN SA3010 SA3 ON SA3.D_E_L_E_T_ = '' AND SA3.A3_COD = SL1.L1_VEND
WHERE 1=1
AND SE3.D_E_L_E_T_ = '' 
AND SE3.E3_BASE < 0 
AND SE3.E3_ZCODRT = '5' 
AND YEAR(SE3.E3_EMISSAO) = 2020
--AND SE3.E3_VEND = '000004'
--AND SE3.E3_CODCLI = '000154'
AND SL1.L1_SERPED	<> ''
AND SL1.L1_DOCPED	<> ''
AND SL1.L1_EMISNF   <> ''
GROUP BY
 SE3.E3_FILIAL,
SE3.E3_SERIE,	
SE3.E3_NUM,	   
SE3.E3_EMISSAO,	
SE3.E3_VENCTO,	
SE3.E3_BASE,
SE3.E3_CODCLI,
SE3.E3_VEND,
SL1.L1_NUMATEN,
SUA.UA_EMISSAO,
SL1.L1_SERPED,
SL1.L1_DOCPED,
SL1.L1_EMISNF,
SL1.L1_CLIENTE,
SA1.A1_NOME,
SL1.L1_VEND,
SA3.A3_NOME,
SE3.E3_ZOBS	

