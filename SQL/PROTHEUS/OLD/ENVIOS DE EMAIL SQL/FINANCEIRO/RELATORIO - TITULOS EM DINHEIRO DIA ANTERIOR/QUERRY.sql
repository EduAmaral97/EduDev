


SELECT
SE1.E1_FILIAL	AS FILIAL,
SE1.E1_PREFIXO	AS PREFIXO,
SE1.E1_NUM		AS TITULO,
SE1.E1_PARCELA	AS PARCELA,
SE1.E1_TIPO		AS TIPO,
SE1.E1_CLIENTE	AS COD_CLI,
SA1.A1_NOME		AS CLIENTE,
SA3.A3_NOME		AS VENDEDOR,
SE1.E1_NATUREZ	AS NATUREZA,
CONCAT(SUBSTRING(SE1.E1_EMISSAO ,7,2), '/', SUBSTRING(SE1.E1_EMISSAO ,5,2), '/', SUBSTRING(SE1.E1_EMISSAO ,1,4)) AS EMISSAO,
SE5.E5_HISTOR	AS HISTORICO,
CONCAT(SUBSTRING(SE1.E1_BAIXA ,7,2),'/',SUBSTRING(SE1.E1_BAIXA ,5,2), '/',SUBSTRING(SE1.E1_BAIXA ,1,4)) AS BAIXA, 
cast(SE1.E1_VALOR as decimal(10, 2))	AS VALOR,
cast(SE1.E1_VALJUR as decimal(10, 2))	AS JUROS,
cast(SE1.E1_DESCONT as decimal(10, 2))	AS DESCONTO,
cast(SE1.E1_VALOR - SE1.E1_SALDO as decimal(10, 2))	AS VLR_BAIXADO,
SE5.E5_BANCO	AS BANCO,
SE5.E5_MOTBX	AS MOT,
SE1.E1_FILORIG	AS FIL_ORI
FROM SE1010 SE1
LEFT JOIN SE5010 SE5 ON SE5.D_E_L_E_T_ = '' AND SE5.E5_FILIAL = SE1.E1_FILIAL AND SE5.E5_PREFIXO = SE1.E1_PREFIXO AND SE5.E5_NUMERO = SE1.E1_NUM AND SE5.E5_PARCELA = SE1.E1_PARCELA AND SE5.E5_TIPO = SE1.E1_TIPO
LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SA1.A1_COD = SE1.E1_CLIENTE AND SA1.A1_LOJA = SE1.E1_LOJA
LEFT JOIN SA3010 SA3 ON SA3.D_E_L_E_T_ = '' AND SA3.A3_COD = SE1.E1_VEND1
WHERE 1=1
AND SE1.D_E_L_E_T_ = '' 
AND SE1.E1_TIPO IN ('R$', 'DH')
AND SE1.E1_EMISSAO = convert(varchar, getdate() -158, 112)



