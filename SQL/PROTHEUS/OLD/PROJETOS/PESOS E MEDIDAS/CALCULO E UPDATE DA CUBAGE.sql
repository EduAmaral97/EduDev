B5_COD,
B5_ALTURA,
B5_LARG,
B5_COMPR,
(B5_ALTURA * B5_LARG * B5_COMPR) AS CUBAGE,
B.B1_ZCUBAGE
FROM SB5010 A, SB1010 B
WHERE 1=1
AND A.D_E_L_E_T_ = ''
AND B.D_E_L_E_T_ = '' 
