/*-----------------------UPDATE CUBAGE-----------------------*/


UPDATE SB1010 SET B1_ZCUBAGE = (B.B5_ALTURA * B.B5_LARG * B.B5_COMPR)
FROM SB1010 A, SB5010 B
WHERE 1=1
AND A.B1_COD = B.B5_COD
--AND A.B1_ZCUBAGE = 0


