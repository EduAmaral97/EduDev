AND A.D_E_L_E_T_ = ''
AND B.D_E_L_E_T_ = ''
AND C.D_E_L_E_T_ = ''
AND A.B1_COD = B.B5_COD
AND A.B1_PROC = C.A2_COD
AND A.B1_COD = D.A5_PRODUTO
