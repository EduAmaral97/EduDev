A.B1_COD AS CODPROD,
A.B1_DESC AS PRODUTO,
C.A2_COD AS COD_FOR,
C.A2_NOME AS FORNECEDOR,
B.B5_ESPESS AS ALTURA ,
B.B5_LARG AS LARGURA ,
B.B5_COMPR AS COMPRIMENTO,
