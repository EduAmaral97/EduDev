




SELECT 
Z_FILIAL_ATM,
Z_NUM_ATM,
Z_DT_EMI_ATM,
Z_COD_CLI,
Z_NOME_CLI,
Z_VENDEDOR,
Z_NUM_PV,
SITUACAO
FROM ZCUBOIND 
WHERE 1=1
AND ESTAGIO = '5'
--AND Z_PROD_REAL <> '40432'
--AND Z_PROD_REAL = '40432'
AND SUBSTRING(Z_GRUPO_PROD,1,2) = 'CF'
GROUP BY 
Z_FILIAL_ATM,
Z_NUM_ATM,
Z_DT_EMI_ATM,
Z_COD_CLI,
Z_NOME_CLI,
Z_VENDEDOR,
Z_NUM_PV,
SITUACAO