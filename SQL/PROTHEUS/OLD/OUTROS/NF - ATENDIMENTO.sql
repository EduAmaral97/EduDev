


SELECT 
SF2.F2_FILIAL	AS FILIAL_NF, 
SF2.F2_SERIE	AS SERIE,	 
SF2.F2_DOC		AS NF,
SD2.D2_COD		AS CODPROD, SD2.D2_ITEM		AS ITEM, SD2.D2_QUANT	AS QTD,
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_FILIAL WHEN SUA700.UA_NUM <> '' THEN SUA700.UA_FILIAL ELSE '' END AS FILIAL_ATM,
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_NUM WHEN SUA700.UA_NUM <> '' THEN SUA700.UA_NUM ELSE '' END AS ATENDIMENTO,
CASE WHEN SUA501.UA_ZCODFRT = '000001' THEN 'Zanotti Refrigeração' WHEN SUA501.UA_ZCODFRT = '000002' THEN 'Cliente Retira Matriz' WHEN SUA501.UA_ZCODFRT = '000003' THEN 'Cliente Retira Dom. Pedro' WHEN SUA501.UA_ZCODFRT >= '000004' THEN 'Transportadora' WHEN SUA700.UA_ZCODFRT = '000001' THEN 'Zanotti Refrigeração' WHEN SUA700.UA_ZCODFRT = '000002' THEN 'Cliente Retira Matriz' WHEN SUA700.UA_ZCODFRT = '000003' THEN 'Cliente Retira Dom. Pedro' WHEN SUA700.UA_ZCODFRT >= '000004' THEN 'Transportadora' ELSE '' END AS TIPO_EXPEDICAO,
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_FILIAL WHEN SUA700.UA_NUM <> '' THEN SUA700.UA_FILIAL ELSE '' END AS FILIAL_ATM,
SA1.A1_COD		AS COD_CLI,
SA1.A1_NOME		AS CLIENTE,
SA1.A1_MUN		AS CIDADE,
SA1.A1_EST	 	AS ESTADO,
SF2.F2_EMISSAO  AS EMISSAO_NF
FROM SF2010 SF2

LEFT JOIN SD2010 SD2 ON SD2.D_E_L_E_T_ = '' AND SD2.D2_FILIAL = SF2.F2_FILIAL AND SD2.D2_SERIE = SF2.F2_SERIE AND SD2.D2_DOC = SF2.F2_DOC AND SD2.D2_CLIENTE = SF2.F2_CLIENTE AND SD2.D2_LOJA = SF2.F2_LOJA
LEFT JOIN SB1010 SB1 ON SB1.D_E_L_E_T_ = '' AND SB1.B1_COD = SD2.D2_COD
LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = '' AND SA1.A1_COD = SF2.F2_CLIENTE AND SA1.A1_LOJA = SF2.F2_LOJA 
LEFT JOIN SL1010 SL1700 ON SL1700.D_E_L_E_T_ = '' AND SL1700.L1_FILIAL = SF2.F2_FILIAL AND SL1700.L1_SERIE = SF2.F2_SERIE AND SL1700.L1_DOC = SF2.F2_DOC AND SL1700.L1_ORCRES <> ''
LEFT JOIN SL1010 SL1501 ON SL1501.D_E_L_E_T_ = '' AND SL1501.L1_FILIAL = SF2.F2_FILIAL AND SL1501.L1_SERIE = SF2.F2_SERIE AND SL1501.L1_DOC = SF2.F2_DOC AND SL1501.L1_ORCRES = ''
LEFT JOIN SUA010 SUA700 ON SUA700.D_E_L_E_T_ = '' AND SUA700.UA_FILIAL = SL1700.L1_FILRES AND SUA700.UA_NUM = SL1700.L1_NUMATEN
LEFT JOIN SUA010 SUA501 ON SUA501.D_E_L_E_T_ = '' AND SUA501.UA_FILIAL = SL1501.L1_FILIAL AND SUA501.UA_NUM = SL1501.L1_NUMATEN
WHERE 1=1
AND SF2.D_E_L_E_T_ = ''
AND SF2.F2_DOC = '000018161'


