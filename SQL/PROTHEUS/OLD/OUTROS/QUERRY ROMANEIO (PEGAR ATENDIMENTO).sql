

SELECT SF2.F2_FILIAL, SF2.F2_SERIE, SF2.F2_DOC, SF2.F2_CLIENTE, 
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_NUM WHEN SUA.UA_NUM <> '' THEN SUA.UA_NUM ELSE '' END AS ATENDIMENTO,
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_ZPRDMLT WHEN SUA.UA_NUM <> '' THEN SUA.UA_ZPRDMLT ELSE '' END AS PROD_MULTI,
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_ZPRDRIB WHEN SUA.UA_NUM <> '' THEN SUA.UA_ZPRDRIB ELSE '' END AS PROD_RIB,
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_ZMONTAG WHEN SUA.UA_NUM <> '' THEN SUA.UA_ZMONTAG ELSE '' END AS MONTAGEM,
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_ZPLOTAG WHEN SUA.UA_NUM <> '' THEN SUA.UA_ZPLOTAG ELSE '' END AS PLOTAGEM,
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_ZCRTPNL WHEN SUA.UA_NUM <> '' THEN SUA.UA_ZCRTPNL ELSE '' END AS CORTE_PAINEL,
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_ZMONTPN WHEN SUA.UA_NUM <> '' THEN SUA.UA_ZMONTPN ELSE '' END AS MONTADOR_PAINEL,
CASE WHEN SUA501.UA_NUM <> '' THEN SUA501.UA_ZCODFRT WHEN SUA.UA_NUM <> '' THEN SUA.UA_ZCODFRT ELSE '' END AS TRANSPORTE,
CASE WHEN SUA501.UA_NUM <> '' THEN SA4501.A4_NOME WHEN SUA.UA_NUM <> '' THEN SA4.A4_NOME ELSE '' END AS TRANSPORTADOR
FROM SF2010 SF2 
LEFT JOIN SC9010 SC9 ON SC9.D_E_L_E_T_ = '' AND SC9.C9_FILIAL = SF2.F2_FILIAL AND SC9.C9_SERIENF = SF2.F2_SERIE AND SC9.C9_NFISCAL = SF2.F2_DOC
LEFT JOIN SL1010 SL1 ON SL1.D_E_L_E_T_ = '' AND SL1.L1_FILIAL = SC9.C9_FILIAL AND SL1.L1_PEDRES = SC9.C9_PEDIDO AND SL1.L1_ORCRES <> ''
LEFT JOIN SUA010 SUA ON SUA.D_E_L_E_T_ = '' AND SUA.UA_FILIAL = SL1.L1_FILRES AND SUA.UA_NUM = SL1.L1_NUMATEN
LEFT JOIN SL1010 SL1501 ON SL1501.D_E_L_E_T_ = '' AND SL1501.L1_FILIAL = SF2.F2_FILIAL AND SL1501.L1_SERIE = SF2.F2_SERIE AND SL1501.L1_DOC = SF2.F2_DOC AND SL1501.L1_ORCRES = ''
LEFT JOIN SUA010 SUA501 ON SUA501.D_E_L_E_T_ = '' AND SUA501.UA_FILIAL = SL1501.L1_FILRES AND SUA501.UA_NUM = SL1501.L1_NUMATEN
LEFT JOIN SA4010 SA4501 ON SA4501.D_E_L_E_T_ = '' AND SA4501.A4_COD = SUA501.UA_ZCODFRT
LEFT JOIN SA4010 SA4 ON SA4.D_E_L_E_T_ = '' AND SA4.A4_COD = SUA.UA_ZCODFRT
WHERE 1=1
AND SF2.D_E_L_E_T_ = '' 
--AND SF2.F2_FILIAL = ''
--AND SF2.F2_SERIE = '001' 
AND SF2.F2_DOC = '000030248'
--AND SUA.UA_NUM = '009031'
GROUP BY 
SF2.F2_FILIAL, SF2.F2_SERIE, SF2.F2_DOC, SF2.F2_CLIENTE, SUA.UA_NUM, SUA501.UA_NUM, SUA501.UA_ZPRDMLT, SUA501.UA_ZPRDRIB, SUA501.UA_ZMONTAG, SUA501.UA_ZPLOTAG, SUA501.UA_ZCRTPNL, SUA501.UA_ZMONTPN, SUA501.UA_ZCODFRT, SUA.UA_ZPRDMLT, SUA.UA_ZPRDRIB, SUA.UA_ZMONTAG, SUA.UA_ZPLOTAG, SUA.UA_ZCRTPNL, SUA.UA_ZMONTPN, SUA.UA_ZCODFRT, SA4501.A4_NOME, SA4.A4_NOME 


