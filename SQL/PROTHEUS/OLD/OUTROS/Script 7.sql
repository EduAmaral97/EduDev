


SELECT
ZZ2.ZZ2_FILIAL,
ZZ3.ZZ3_CODPRD,
ZZ3.ZZ3_CUSTD	AS RETROATIVO,
SB1.B1_CUSTD	AS ATUAL,
ZZ2.ZZ2_DATA,
ZZ2.ZZ2_HORA
FROM ZZ2010 ZZ2
LEFT JOIN ZZ3010 ZZ3 ON ZZ3.D_E_L_E_T_ = '' AND ZZ3.ZZ3_FILIAL = ZZ2.ZZ2_FILIAL AND ZZ3.ZZ3_COD = ZZ2.ZZ2_CODIGO
LEFT JOIN SB1010 SB1 ON SB1.D_E_L_E_T_ = '' AND SB1.B1_COD = ZZ3.ZZ3_CODPRD
WHERE 1=1
AND ZZ2.D_E_L_E_T_ = ''
--AND ZZ2.ZZ2_DATA = '20210217'
AND ZZ3.ZZ3_CODPRD = 'LB012'
ORDER BY   ZZ2.ZZ2_DATA




SELECT 
A.L1_FILIAL,
A.L1_NUMATEN,
A.L1_EMISNF,
A.L1_DOC,
A.L1_DOCPED,
B.L2_PRODUTO,
B.L2_ZCSTREP
FROM SL1010 A 
LEFT JOIN SL2010 B ON B.D_E_L_E_T_ = '' AND B.L2_FILIAL = A.L1_FILIAL AND B.L2_NUM = A.L1_NUM
WHERE 1=1
AND A.D_E_L_E_T_ = ''
AND YEAR(A.L1_EMISSAO) = '2021' 
